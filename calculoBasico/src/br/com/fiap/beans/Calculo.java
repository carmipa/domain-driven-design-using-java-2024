package br.com.fiap.beans;

public class Calculo {

	int numero1;
	int numero2;
	int soma;
	int subtracao;
	int multiplicacao;
	int divisao;
	int restoDivisao;

	public int getNumero1() {
		return numero1;
	}

	public void setNumero1(int numero1) {
		this.numero1 = numero1;
	}

	public int getNumero2() {
		return numero2;
	}

	public void setNumero2(int numero2) {
		this.numero2 = numero2;
	}

	public int getSoma() {
		return soma;
	}

	public void setSoma(int soma) {
		this.soma = soma;
	}

	public int getSubtracao() {
		return subtracao;
	}

	public void setSubtracao(int subtracao) {
		this.subtracao = subtracao;
	}

	public int getMultiplicacao() {
		return multiplicacao;
	}

	public void setMultiplicacao(int multiplicacao) {
		this.multiplicacao = multiplicacao;
	}

	public int getDivisao() {
		return divisao;
	}

	public void setDivisao(int divisao) {
		this.divisao = divisao;
	}

	public int getRestoDivisao() {
		return restoDivisao;
	}

	public void setRestoDivisao(int restoDivisao) {
		this.restoDivisao = restoDivisao;
	}

}
